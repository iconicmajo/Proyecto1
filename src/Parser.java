public class Parser {

    public String arithmeticOperations(String function){
        String tempFunction = "+ 5 6";

        for (int i = 0; i < function.length() ; i++) {
            switch (tempFunction.charAt(i)){
                case '+': break;
                case '-': break;
                case '*': break;
                case '/': break;
            }

        }

        return  "";
    }
}
